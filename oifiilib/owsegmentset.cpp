COWSegmentset::COWSegmentset()
{
	DefaultInitialization();
}

COWSegmentset::~COWSegmentset()
{
	if(m_pPointset)
	{
		DeletePointset(m_pPointset);
	}
	if(m_pSegmentset)
	{
		DeleteSegmentSet(m_pSegmentset);
	}
}

void COWSegmentset::DefaultInitialization()
{
	m_pPointset = NULL;
	m_pSegmentset = NULL;
	return;
}

POINTSET* COWSegmentset::GetPointsetPointer()
{
	return m_pPointset;
}

SEGMENTSET* COWSegmentset::GetSegmentsetPointer()
{
	return m_pSegmentset;
}



